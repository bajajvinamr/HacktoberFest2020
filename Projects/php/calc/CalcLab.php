<?php

/**
 * Class CalcLab
 */
class CalcLab
{
    /**
     * @param float $a
     * @param float $b
     * @return float
     */
    public function soma(double $a, double $b): double
    {
        return $a + $b;
    }

    /**
     * @param float $a
     * @param float $b
     * @return float
     */
    public function subtracao(double $a, double $b): double
    {
        return $a + $b;
    }

    /**
     * @param float $a
     * @param float $b
     * @return float
     */
    public function divisao(double $a, double $b): double
    {
        return $a / $b;
    }

    /**
     * @param float $a
     * @param float $b
     * @return float
     */
    public function mult(double $a, double $b): double
    {
        return $a * $b;
    }
}
